// ─── Runner ────────────────────────────────────────────────────────────────────
async function seedProducts() {
  const uri = process.env.MONGODB_URI;
  if (!uri) {
    console.error("❌  MONGODB_URI not set in .env — aborting.");
    process.exit(1);
  }

  console.log("🔌  Connecting to MongoDB…");
  await mongoose.connect(uri);
  console.log("✅  Connected.\n");

  const ALL = [...CATALOGUE_PRODUCTS, ...DEMO_PRODUCTS];
  let inserted = 0,
    updated = 0,
    failed = 0;

  for (const data of ALL) {
    try {
      const existing = await Product.findOne({ slug: data.slug });
      if (existing) {
        await Product.updateOne({ slug: data.slug }, { $set: data });
        console.log(`  🔄  updated  → ${data.slug}`);
        updated++;
      } else {
        await Product.create(data);
        console.log(`  ✨  inserted → ${data.slug}`);
        inserted++;
      }
    } catch (err: any) {
      console.error(`  ❌  FAILED   → ${data.slug}  (${err.message})`);
      failed++;
    }
  }

  console.log(
    `\n🌱  Done — inserted: ${inserted}, updated: ${updated}, failed: ${failed}`,
  );
  console.log(`    Total in DB: ${inserted + updated} / ${ALL.length}`);
  await mongoose.disconnect();
  process.exit(failed > 0 ? 1 : 0);
}

seedProducts().catch((err) => {
  console.error("💥  Crash:", err);
  process.exit(1);
});
